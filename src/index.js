function movePlayer () {
  const direction = window.appState.player.direction
  if (direction === 'right') {
    var x = window.appState.player.x
    var y = window.appState.player.y++
    window.appState.board[x][y] = 'p'
    window.appState.board[x][y - 1] = 0
  }
}

