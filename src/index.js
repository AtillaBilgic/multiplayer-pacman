export function createPlayer (id, name, color,) {
  let direction, x, y = null

  if (id === 0) {
    direction = 'right'
    x = 2
    y = 1
  }
  if (id === 1) {
    direction = 'left'
    x = 27
    y = 1
  }
  if (id === 2) {
    direction = 'right'
    x = 1
    y = 29
  }
  if (id === 3) {
    direction = 'left'
    x = 27
    y = 29
  }

  return {
    direction,
    x,
    y,
    id,
    name,
    color,
    speed: 1,
    score: 0,
    isWeak: false,
    hasPower: false,
    isDead: false,
    count: 1,
    isGameReady: false
  }
}

